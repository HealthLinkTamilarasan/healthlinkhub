import User from '../models/User.js';
import generateId from '../utils/idGenerator.js';
import jwt from 'jsonwebtoken';

// Generate JWT
const generateToken = (id, role, roleId, userId) => {
    return jwt.sign({ id, role, roleId, userId }, process.env.JWT_SECRET, {
        expiresIn: '30d',
    });
};

// @desc    Register a new user
// @route   POST /api/auth/register
// @access  Public
export const registerUser = async (req, res) => {
    try {
        const { fullName, email, userId, password, phone, role, confirmPassword, ...roleData } = req.body;

        // Validate required fields
        if (!fullName || !email || !userId || !password || !role) {
            return res.status(400).json({ message: 'Please fill in all required fields (fullName, email, userId, password, role)' });
        }

        // Check if email exists
        const emailExists = await User.findOne({ email });
        if (emailExists) {
            return res.status(400).json({ message: 'Email already exists' });
        }

        // Check if userId exists
        const userIdExists = await User.findOne({ userId });
        if (userIdExists) {
            return res.status(400).json({ message: 'User ID already exists' });
        }

        // Generate System ID (Patient ID -> roleId)
        // Patient ID is generated by system. UserId is manual.
        const roleId = await generateId(role);

        const user = await User.create({
            fullName,
            email,
            userId, // Manual input
            password,
            phone,
            role,
            roleId, // System generated
            ...roleData,
        });

        if (user) {
            const userObj = user.toObject();
            delete userObj.password;
            res.status(201).json({
                ...userObj,
                token: generateToken(user._id, user.role, user.roleId, user.userId),
            });
        } else {
            res.status(400).json({ message: 'Invalid user data' });
        }
    } catch (error) {
        console.error('Registration error:', error);

        // Handle Mongoose validation errors
        if (error.name === 'ValidationError') {
            const messages = Object.values(error.errors).map(e => e.message);
            return res.status(400).json({ message: messages.join(', ') });
        }

        // Handle duplicate key error (race condition)
        if (error.code === 11000) {
            const field = Object.keys(error.keyPattern)[0];
            return res.status(400).json({ message: `${field} already exists` });
        }

        res.status(500).json({ message: error.message });
    }
};

// @desc    Auth user & get token
// @route   POST /api/auth/login
// @access  Public
export const loginUser = async (req, res) => {
    try {
        const { identifier, password, role } = req.body;

        // 1. Search user by userId, email, OR roleId (Patient ID)
        const user = await User.findOne({
            $or: [{ email: identifier }, { userId: identifier }, { roleId: identifier }]
        });

        // 2. If user not found
        if (!user) {
            return res.status(404).json({ message: 'User not found' });
        }

        // 3. Verify Password
        const isMatch = await user.matchPassword(password);
        if (!isMatch) {
            return res.status(401).json({ message: 'Incorrect password' });
        }

        // 4. Verify Role (If role provided in request, check it)
        if (role && user.role !== role) {
            return res.status(403).json({ message: 'Access denied for this role' });
        }

        // 5. Special check: Patients cannot login with PatientId (roleId)
        // Although our search query above uses $or on email/userId, if 'identifier' happened to be equal to roleId
        // we should ensure it wasn't matched via obscure means or if requirements meant "Don't accept patientId as input".
        // Since we explicitly filtered by email/userId fields, if the user entered their Patient ID and it's NOT their userId, the initial findOne would fail (User not found).
        // So the logic holds. But just in case userId == roleId (unlikely but possible if user sets it), we proceed.

        // Return Success — include ALL user fields so frontend has full data
        const userObj = user.toObject();
        delete userObj.password;
        res.json({
            ...userObj,
            token: generateToken(user._id, user.role, user.roleId, user.userId),
        });

    } catch (error) {
        res.status(500).json({ message: error.message });
    }
};
